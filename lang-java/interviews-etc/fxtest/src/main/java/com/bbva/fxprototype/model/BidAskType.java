package com.bbva.fxprototype.model;

/**
 * Bid Or Ask Enum.
 * The Bid price is what a market participant is prepared to PAY
 * for a ccy pair (i.e. to receive the Base CCY).
 * The Ask price is what a market participant is prepared to SELL
 * the ccy pair at (i.e. to receive the Quote CCY).
 */
public enum BidAskType {
    BID,
    ASK
}
